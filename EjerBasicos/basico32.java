public class Basico32 {
    public static void main(String[] args) {
        
        double x = 88;
        double y = 3.5;
        double z = (-5.2);

        System.out.println(x + y + z);
        System.out.println(2 * y + 3 * (x - z));
        System.out.println(x / y);
        System.out.println(x % y);
        System.out.println(x / (y + z));
        System.out.println(3 * x - z - 2 * x);
        System.out.println(2 * x / 5 % y);
    }
}
